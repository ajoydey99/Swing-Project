    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String query="INSERT INTO student VALUES(?,?,?,?,?,?,?)";
        try
        {
           pst = conn.prepareStatement(query);
           
           pst.setInt(1, code);
           pst.setString(2,jTextField2.getText().toUpperCase());
           pst.setString(3,(String)jComboBox1.getSelectedItem());
           pst.setString(4,jTextField3.getText().toUpperCase());
           pst.setString(5,(String)jComboBox2.getSelectedItem());
           pst.setInt(6,Integer.parseInt(jTextField4.getText()));
           pst.setString(7,jTextField5.getText());
           
           pst.execute();
           JOptionPane.showMessageDialog(null,"Student Record Added.");
           
           code = rd.nextInt(900)+100;
           jTextField1.setText(String.valueOf(code));
           jTextField2.setText("");
           jTextField3.setText("");
           jTextField4.setText("");
           jTextField5.setText("");
           
           pst.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

