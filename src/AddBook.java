    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String query="INSERT INTO book VALUES(?,?,?,?,?,?)";
        try
        {
           pst = conn.prepareStatement(query);
           
           pst.setInt(1, id);
           pst.setString(2,jTextField2.getText().toUpperCase());
           pst.setString(3,(String)jComboBox1.getSelectedItem());
           pst.setString(4,jTextField3.getText().toUpperCase());
           pst.setString(5,jTextField4.getText().toUpperCase());
           pst.setInt(6,Integer.parseInt(jTextField5.getText()));
           
           pst.execute();
           JOptionPane.showMessageDialog(null,"New Book Added.");
           
           id=rd.nextInt(900)+100;
           jTextField1.setText(String.valueOf(id));
           jTextField2.setText("");
           jTextField3.setText("");
           jTextField4.setText("");
           jTextField5.setText("");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

