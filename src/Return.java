    public void returnUpdate()
    {
        try
        {
            String query="INSERT INTO Return VALUES(?,?,?,?,?,?,?,?,?)";
            pst=conn.prepareStatement(query);
            
            pst.setInt(1,Integer.parseInt(jTextField1.getText()));
            pst.setString(2,jTextField2.getText());
            pst.setString(3,jTextField3.getText());
            pst.setString(4,jTextField4.getText());
            pst.setInt(5,Integer.parseInt(jTextField5.getText()));
            pst.setString(6,jTextField6.getText());
            pst.setInt(7,Integer.parseInt(jTextField7.getText()));
            pst.setString(8,jTextField8.getText());
            pst.setString(9,((JTextField)jDateChooser1.getDateEditor().getUiComponent()).getText());
            
            pst.executeUpdate();
            pst.close();
            JOptionPane.showMessageDialog(null,"Book Returned to Library");
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField7.setText("");
            jTextField8.setText("");
            jDateChooser1.setDate(null);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }
