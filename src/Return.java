    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(jTextField5.getText());
        try
        {
            String query="SELECT * FROM Book JOIN Issue ON Issue.BookID=Book.id WHERE StudentID="+id;
            pst=conn.prepareStatement(query);
            rs=pst.executeQuery();
            if(rs.next())
            {
                jTextField1.setText(rs.getString(1));
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(5));
                jTextField6.setText(rs.getString(10));
                jTextField7.setText(rs.getString(11));
                jTextField8.setText(rs.getString(12));
                
                rs.close();
                pst.close();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Book not issued with this ID");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

